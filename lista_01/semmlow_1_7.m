close all
clear variables
clc

% Hard-coded values
%--------------------------------------------------------
TT = 1;
f = 5;
N = 1000;
%--------------------------------------------------------

% Variables 
%--------------------------------------------------------
fs = N/TT;
Ts = 1/fs;
t = (0:N-1)*Ts;
x = sin(2*pi*f*t);

%--------------------------------------------------------
plot(t,x,'--r');
hold on
t_7 = (0:1/7:TT);
x_7 = sin(2*pi*f*t_7);
plot(t_7,x_7,"*g");

x2 = sin(2*pi*2*t+pi);
plot(t,x2);
